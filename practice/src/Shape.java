public class Shape {
        public static void main(String[] args) {
                     for(int i=1;i<=5;i++){
                         for(int j=5-i;j>=0;j--){//j--控制空格的数量
                             System.out.print(" ");
                                         }
                            for(int k=1;k<=i;k++){
                                     System.out.print("* ");
                                 }
                             System.out.println(" ");
                         }
                 }
    }

